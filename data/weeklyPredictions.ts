export function getCurrentWeekReport(): WeeklyReport | undefined {
  const now = new Date();
  return weeklyReports.find((r) => {
    const start = new Date(r.weekStart);
    const end = new Date(r.weekEnd);
    return now >= start && now <= end;
  });
}

export function getPastWeekReports(): WeeklyReport[] {
  const now = new Date();
  return weeklyReports
    .filter((r) => new Date(r.weekEnd) < now)
    .sort(
      (a, b) =>
        new Date(b.weekStart).getTime() - new Date(a.weekStart).getTime(),
    );
}

export function getWeeklyReportByWeek(
  year: number,
  week: number,
): WeeklyReport | undefined {
  return weeklyReports.find((r) => r.year === year && r.weekNumber === week);
}
